package com.company;

public class Activity09{

    public static void main(String[] args) {

        //Declaring variables
        int x = 1;
        int y = 2;
        int p = 8;
        int q = 7;

        int result = x + y; // result is now 3
        System.out.println(result);

        result -= x; // result is now 2
        System.out.println(result);

        result *= y; // result is now 4
        System.out.println(result);

        result /= y; // result is now 2
        System.out.println(result);

        result += p; // result is now 10
        System.out.println(result);

        result %= q; // result is now 3
        System.out.println(result);

    }
}
